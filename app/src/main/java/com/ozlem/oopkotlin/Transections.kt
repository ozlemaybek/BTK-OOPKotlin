package com.ozlem.oopkotlin

class Transections {

    // Static Polymorphism:

    fun multiplicaiton() : Int {
        return 0
    }

    fun multiplicaiton(a : Int , b : Int) : Int{
        return a * b
    }

    fun multiplication(a: Int , b : Int , c : Int) : Int{
        return a * b * c
    }
}
